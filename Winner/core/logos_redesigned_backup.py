# ============================================
# ANSI COLOR CODES FOR EACH THEME
# ============================================
# These codes are used to colorize the logo when displayed

THEME_COLORS: Dict[str, Tuple[str, str]] = {
    # Theme name: (foreground ANSI, background ANSI)
    "dark":     ("\033[97m", "\033[40m"),       # White on Black
    "light":    ("\033[30m", "\033[47m"),       # Black on White
    "neon":     ("\033[95m", "\033[40m"),       # Bright Magenta on Black
    "hacker":   ("\033[92m", "\033[40m"),       # Bright Green on Black
    "zen":      ("\033[96m", "\033[40m"),       # Bright Cyan on Black
    "retro":    ("\033[93m", "\033[40m"),       # Bright Yellow on Black
    "ocean":    ("\033[94m", "\033[40m"),       # Bright Blue on Black
    "sunset":   ("\033[33m", "\033[40m"),       # Orange on Black
    "midnight": ("\033[34m", "\033[40m"),       # Blue on Black
    "calm":     ("\033[37m", "\033[40m"),       # Light Gray on Black
    "warning":  ("\033[91m", "\033[40m"),       # Bright Red on Black
}

RESET_CODE = "\033[0m"


def get_logo(theme_name: str) -> str:
    """
    Get the ASCII logo for a specific theme.
    
    Args:
        theme_name: Name of the theme (e.g., 'dark', 'hacker', 'ocean')
        
    Returns:
        The ASCII art logo string for that theme, or default 'dark' logo
    """
    return THEME_LOGOS.get(theme_name.lower(), THEME_LOGOS["dark"])


def get_colored_logo(theme_name: str, use_theme_bg: bool = False) -> str:
    """
    Get the ASCII logo with ANSI color codes applied.
    
    Args:
        theme_name: Name of the theme
        use_theme_bg: If True, also apply background color
        
    Returns:
        Colorized ASCII logo string with ANSI codes
    """
    logo = get_logo(theme_name)
    colors = THEME_COLORS.get(theme_name.lower(), THEME_COLORS["dark"])
    
    fg_code = colors[0]
    bg_code = colors[1] if use_theme_bg else ""
    
    # Apply color to each line
    colored_lines = []
    for line in logo.split('\n'):
        colored_lines.append(f"{fg_code}{bg_code}{line}{RESET_CODE}")
    
    return '\n'.join(colored_lines)


def print_logo(theme_name: str, center: bool = True, terminal_width: int = 80) -> None:
    """
    Print the theme logo to the terminal.
    
    Args:
        theme_name: Name of the theme
        center: If True, center the logo in the terminal
        terminal_width: Width of terminal for centering
    """
    import shutil
    
    # Get actual terminal width if possible
    try:
        terminal_width = shutil.get_terminal_size().columns
    except:
        pass
    
    logo = get_colored_logo(theme_name)
    
    if center:
        centered_lines = []
        for line in logo.split('\n'):
            # Calculate visible length (excluding ANSI codes)
            visible_length = len(line.replace('\033[97m', '').replace('\033[40m', '')
                                    .replace('\033[30m', '').replace('\033[47m', '')
                                    .replace('\033[95m', '').replace('\033[92m', '')
                                    .replace('\033[96m', '').replace('\033[93m', '')
                                    .replace('\033[94m', '').replace('\033[33m', '')
                                    .replace('\033[34m', '').replace('\033[37m', '')
                                    .replace('\033[91m', '').replace('\033[0m', ''))
            
            padding = max(0, (terminal_width - visible_length) // 2)
            centered_lines.append(' ' * padding + line)
        
        logo = '\n'.join(centered_lines)
    
    print(logo)


def get_all_theme_names() -> list:
    """Get list of all available theme names."""
    return list(THEME_LOGOS.keys())


def get_logo_preview(theme_name: str) -> str:
    """
    Get a short preview of the logo (first few lines) for menu display.
    
    Args:
        theme_name: Name of the theme
        
    Returns:
        Short preview string (3-4 lines)
    """
    logo = get_logo(theme_name)
    lines = [line for line in logo.split('\n') if line.strip()]
    
    # Return first 3 non-empty lines as preview
    preview_lines = lines[:3] if len(lines) >= 3 else lines
    return '\n'.join(preview_lines)


